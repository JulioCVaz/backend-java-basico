package exercicios;


public class Exercicio13 {

	public static void main(String[] args) {
		int valor = 50;
		float valorf = 2.99f;
		
		if(valor == valorf) {
			System.out.println("São iguais");
		}else if(valor != valorf) {
			System.out.println("Valores são diferentes");
		}else {
			System.out.println("Valor incorreto!");
		}

	}

}
