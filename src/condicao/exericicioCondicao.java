package condicao;

public class exericicioCondicao {

	public static void main(String[] args) {
		int number = 50;
		
		if(number < 15){
			System.out.println("O Valor é menor que 15");
		}else if(number > 100){
			System.out.println("O valor é maior que 100");
		}else{
			System.out.println("Valor fica entre 15 e 100 !");
		}

	}

}
